import Foundation

extension Int32 {
    
    func toInt() -> Int {
        return Int(self)
    }
}


extension Int {
    
    func toInt32() -> Int32 {
        return Int32(self)
    }
    
    func toCGFloat() -> CGFloat {
        return CGFloat(self)
    }
}
